package models;

/**
 *
 * @author Administrator
 */
public class staffModel {

    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the job
     */
    public String getJob() {
        return job;
    }

    /**
     * @param job the job to set
     */
    public void setJob(String job) {
        this.job = job;
    }

    /**
     * @return the age
     */
    public int getAge() {
        return age;
    }

    /**
     * @param age the age to set
     */
    public void setAge(int age) {
        this.age = age;
    }

    /**
     * @return the gender
     */
    public String getGender() {
        return gender;
    }

    /**
     * @param gender the gender to set
     */
    public void setGender(String gender) {
        this.gender = gender;
    }

    public staffModel(String name, String job, int age, String gender) {
        this.name = name;
        this.job = job;
        this.age = age;
        this.gender = gender;
    }

    public staffModel() {
    }
    private String name;
    private String job;
    private int age;
    private String gender;
}
